// Write a function called “getLengthOfThreeWords”.

// Given 3 words, “getLengthOfThreeWords” returns the sum of their lengths.

getLengthOfThreeWords = (word1, word2, word3) => word1.length + word2.length + word3.length

const output = getLengthOfThreeWords("cake", "cookie", "pizza")
console.log(output)